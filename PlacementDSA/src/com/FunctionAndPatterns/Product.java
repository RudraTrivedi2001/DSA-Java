package com.FunctionAndPatterns;

public class Product
{
       public static int product(int a, int b) 
       
       {
    	   int x = a * b;
    	   
    	   return x;
    	   
       }
       
       
       
       public static void main(String[] args) {
		
    	   int p = product(4, 5);
    	   System.out.println(p);
    	   
    	   
	}
}
